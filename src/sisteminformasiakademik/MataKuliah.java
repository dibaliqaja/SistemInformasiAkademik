    public void tampil_kombo()
    {
        try {
        st = con.createStatement();
        String sql="Select * from prodi"; /*query yang dijalankan*/
        rs=st.executeQuery(sql);
        while(rs.next()){
            jComboBox1.addItem(rs.getString("nama_prodi"));
        }
        rs.close();
        st.close();
         
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

